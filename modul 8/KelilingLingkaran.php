<?php
// Menghitung keliling lingkaran dengan jari-jari 15 cm
// Rumus keliling lingkaran: K = 2 * pi * r

$jari_jari = 15;
$pi = 3.14159265358979;

$keliling = 2 * $pi * $jari_jari;

echo "<h2>Menghitung Keliling Lingkaran</h2>";
echo "<p>Jari-jari (r) = $jari_jari cm</p>";
echo "<p>π (pi)        = $pi</p>";
echo "<p>Rumus         : K = 2 × π × r</p>";
echo "<p><strong>Keliling Lingkaran = 2 × $pi × $jari_jari = $keliling cm</strong></p>";
?>
